package org.techtown.dontlate;

public class ArsItems {
    String arsCode;
    String posX;
    String posY;
    String stId;
    String stNm;
    String tmX;
    String tmY;

    public String getArsCode(){
        return arsCode;
    }

    public String getPosX(){
        return posX;
    }

    public String getPosY(){
        return posY;
    }

    public String getStId(){
        return stId;
    }

    public String getStNm(){
        return stNm;
    }

    public String getTmX(){
        return tmX;
    }

    public String getTmY(){
        return tmY;
    }

    public void setArsCode(String arsCode){
        this.arsCode = arsCode;
    }

    public void setPosX(String posX){
        this.posX = posX;
    }

    public void setPosY(String posY){
        this.posY = posY;
    }

    public void setStId(String stId){
        this.stId = stId;
    }

    public void setStNm(String stNm){
        this.stNm = stNm;
    }

    public void setTmX(String tmX){
        this.tmX = tmX;
    }

    public void setTmY(String tmY){
        this.tmY = tmY;
    }
}
